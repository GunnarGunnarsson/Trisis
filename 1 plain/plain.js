// Define the transformation scale here (two scale functions in MV.js)
function scale4( x, y, z )
{
    if ( Array.isArray(x) && x.length == 3 ) {
        z = x[2];
        y = x[1];
        x = x[0];
    }

    var result = mat4();
    result[0][0] = x;
    result[1][1] = y;
    result[2][2] = z;

    return result;
}


function render()
{
    gl.clear( gl.COLOR_BUFFER_BIT | gl.DEPTH_BUFFER_BIT);

    var ctm = mat4();
    ctm = mult( ctm, rotate( parseFloat(spinX), [1, 0, 0] ) );
    ctm = mult( ctm, rotate( parseFloat(spinY), [0, 1, 0] ) ) ;

    // Kubburinn skilgreindur
    kubbur = mult( ctm, translate( xplacement, yplacement, zplacement ) ); // Kassanum hliðrað
    kubbur = mult( kubbur, scale4( 0.05, 0.05, 0.05 ) ); // Kassinn skalaður í rétt hlutföll
    gl.uniformMatrix4fv(matrixLoc, false, flatten(kubbur));
    gl.drawArrays( gl.TRIANGLES, 0, NumVertices );

    requestAnimFrame( render );
}

